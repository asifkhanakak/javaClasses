package com.class02;

public class VariableDecleration {
	
	public static void main(String[] args) {
		
		// Deceleration and assigning value at the same time
		int num1=11;
		int num2=22;
		System.out.println(num1+" "+num2);
		
		// Declaring variable and then assigning it the value
		int n1;
		int n2;
		
		n1=34;
		n2=55;
		System.out.println(n1+" "+n2);
		
		// Declaring variable in one line
		int a,b,c;
		a=1;
		b=2;
		c=3;
		System.out.println(a+" "+b+" "+c);
		System.out.println(a);
		a=num2;
		
		System.out.println(a);
				
	}

}
