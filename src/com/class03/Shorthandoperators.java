package com.class03;

public class Shorthandoperators {
	public static void main(String[] args) {
	
	int num3=1000;
	num3+=1000;
	System.out.println(num3);
	
	int num4=50;
	num4+=20;
	System.out.println(num4);
	
	num4-=20;
	System.out.println(num4);
	
	num4%=5;
	System.out.println(num4);
			
	
	
	}
}
