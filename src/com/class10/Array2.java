package com.class10;

public class Array2 {

	public static void main(String[] args) {
		
		String[] names=new String[6];
		names[0]="Samir";
		names[1]="Zubair";
		names[2]="Zain";
		names[3]="Asif";
		
		System.out.println(names[3]);
		System.out.println(names[4]);
		
	String	groupnames[]= {"Asif","Zain","Samir","Zubair"};
	
	System.out.println(groupnames[0]);
	System.out.println(groupnames[1]);
	System.out.print(groupnames[0]+" "+groupnames[1]+" "+groupnames[2]+" "+groupnames[3]);

	}

}
